    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.getRequestDispatcher("forgotPassword.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession();

        String get_email = request.getParameter("email");
        Customer customer = new Customer();
        customer.setEmail(get_email);
        SendEmail sendEmail = new SendEmail();
        String otp = sendEmail.getRandom();
        session.setAttribute("forgotPasswordOTP", otp);

        if (sendEmail.sendEmail(customer, otp)) {
            session.setAttribute("forgotPasswordOTP", otp);
            session.setAttribute("emailForgot", customer.getEmail());
            response.sendRedirect("/forgot-password-verify");
        } else {
            request.setAttribute("error", "Failed to send email");
            request.getRequestDispatcher("forgotPassword.jsp").forward(request, response);
        }
    }

